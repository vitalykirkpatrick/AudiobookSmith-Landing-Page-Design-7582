/**
 * Generates a unique vector sketch avatar URL based on the voice name and gender.
 * Uses DiceBear 'Notionists' style.
 * 
 * @param {string} name - The name of the voice
 * @param {string} gender - 'Male', 'Female', or 'Neutral'
 * @param {Array} tags - Optional tags
 * @returns {string} - The URL to the SVG avatar
 */
export const getAvatarUrl = (name, gender = 'Neutral', tags = []) => {
  // Check if there's a hardcoded override (fallback for legacy data)
  if (voiceMetadata[name] && voiceMetadata[name].avatar) {
    return voiceMetadata[name].avatar;
  }

  // Sanitize name for seed
  const cleanName = name.trim().replace(/[^a-zA-Z0-9]/g, '');
  
  // Append gender to seed to ensure "Adam" (Male) generates differently than if it were "Adam" (Female)
  // This drastically changes the random seed based on gender.
  const seed = `${cleanName}-${gender}`;
  
  // Base parameters for 'Notionists' style
  let params = `seed=${seed}&backgroundColor=e5e7eb,d1d5db,9ca3af&radius=50`;
  
  // Gender-specific tweaks
  if (gender.toLowerCase() === 'female') {
    // Force no beard for females to avoid gender confusion in the sketch
    params += '&beardProbability=0';
  } else if (gender.toLowerCase() === 'male') {
    // For males, we allow natural generation, but the seed includes "Male"
    // which helps randomizing away from female-looking defaults if the name was ambiguous.
    // We can also slightly increase beard prob if desired, but 0-100 default is usually mix.
    // Let's ensure glasses probability is moderate for variation.
    params += '&glassesProbability=30';
  }

  return `https://api.dicebear.com/9.x/notionists/svg?${params}`;
};