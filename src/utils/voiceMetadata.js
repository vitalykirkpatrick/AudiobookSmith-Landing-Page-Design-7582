export const getAvatarUrl = (name, gender = 'Neutral', tags = []) => {
  const cleanName = name.trim().replace(/[^a-zA-Z0-9]/g, '');
  const seed = `${cleanName}-${gender}`;
  let params = `seed=${seed}&backgroundColor=e5e7eb,d1d5db,9ca3af&radius=50`;

  if (gender.toLowerCase() === 'female') {
    params += '&beardProbability=0';
  } else if (gender.toLowerCase() === 'male') {
    params += '&glassesProbability=30';
  }

  return `https://api.dicebear.com/9.x/notionists/svg?${params}`;
};